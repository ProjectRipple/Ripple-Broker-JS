import socket
import time

UDP_IP = "127.0.0.1"
UDP_PORT = 5690

while True:
	source = bytearray([0x1,0x2,0x3,0x4,0x5,0x6,0x7,0x8,0x9,0x10,0x11,0x12,0x13,0x14,0x15,0x16,0x17,0x18,0x19,0x20,0x21,0x22,0x23])
	sock = socket.socket(socket.AF_INET, socket.SOCK_DGRAM)
	sock.sendto(source, (UDP_IP, UDP_PORT))
	time.sleep(2)	
